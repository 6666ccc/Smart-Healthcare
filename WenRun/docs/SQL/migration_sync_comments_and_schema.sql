-- 1. registration.registrant_user_id：不存在则添加
SET @sql_add_registrant = (
    SELECT IF(
        EXISTS(
            SELECT 1 FROM information_schema.COLUMNS
            WHERE TABLE_SCHEMA = DATABASE()
              AND TABLE_NAME = 'registration'
              AND COLUMN_NAME = 'registrant_user_id'
        ),
        'SELECT ''registration.registrant_user_id 已存在，跳过添加''',
        'ALTER TABLE registration ADD COLUMN registrant_user_id BIGINT NULL COMMENT ''挂号人用户 ID（谁帮忙挂的号）'' AFTER cashier_id'
    )
);
PREPARE stmt FROM @sql_add_registrant;
EXECUTE stmt;
DEALLOCATE PREPARE stmt;

-- 2. registrant_user_id：统一为 bigint + 最新注释（兼容旧库 int 类型）
ALTER TABLE registration
    MODIFY COLUMN registrant_user_id BIGINT NULL
        COMMENT '挂号人用户 ID（谁帮忙挂的号，前端查自己的+帮别人挂的）';

